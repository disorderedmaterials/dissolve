extern const std::vector<Vec3<double>> refCoords;

TEST(CellsTest, Basic)
{
    CoreData coreData;
    Dissolve dissolve(coreData);
    dissolve.setPairPotentialRange(9.0);
    dissolve.setAutomaticChargeSource(false);
    dissolve.setForceChargeSource(true);
    PairPotential::setShortRangeTruncationScheme(PairPotential::NoShortRangeTruncation);

    // Add atom types and LJ pair potentials (only one real one - between Ar and OW
    auto arType = coreData.addAtomType(Elements::Ar);
    arType->setName("Ar");
    arType->interactionPotential().setForm(ShortRangeFunctions::Form::None);
    auto hType = coreData.addAtomType(Elements::H);
    hType->setName("HW");
    hType->interactionPotential().setForm(ShortRangeFunctions::Form::None);
    auto oType = coreData.addAtomType(Elements::O);
    oType->setName("OW");
    oType->interactionPotential().setForm(ShortRangeFunctions::Form::None);

    dissolve.addPairPotential(arType, arType)->interactionPotential().setForm(Functions1D::Form::None);
    dissolve.addPairPotential(hType, hType)->interactionPotential().setForm(Functions1D::Form::None);
    dissolve.addPairPotential(oType, oType)->interactionPotential().setForm(Functions1D::Form::None);
    dissolve.addPairPotential(hType, oType)->interactionPotential().setForm(Functions1D::Form::None);
    dissolve.addPairPotential(arType, hType)->interactionPotential().setForm(Functions1D::Form::None);

    // Set up pseudo-species
    auto *argon = coreData.addSpecies();
    argon->setName("Argon");
    argon->addAtom(Elements::Ar, {0.0, 0.0, 0.0}, 0.0);
    argon->atom(0).setAtomType(arType);
    auto *water = coreData.addSpecies();
    water->setName("Water");
    water->addAtom(Elements::H, {-1.0, 0.0, 0.0}, 0.0);
    water->addAtom(Elements::O, {0.0, 0.0, 0.0}, 0.0);
    water->addAtom(Elements::H, {-cos(109.5), sin(109.5), 0.0}, 0.0);
    water->atom(0).setAtomType(hType);
    water->atom(1).setAtomType(oType);
    water->atom(2).setAtomType(hType);
    water->addBond(0, 1);
    water->addBond(1, 2);

    // Setup Configuration
    auto *cfg = coreData.addConfiguration();
    cfg->createBoxAndCells({20, 20, 20}, {90, 90, 90}, false, dissolve.pairPotentialRange());
    cfg->cells().generate(cfg->box(), 7.0, dissolve.pairPotentialRange());
    cfg->addMolecule(argon);
    for (auto n = 0; n < 267; ++n)
        cfg->addMolecule(water);
    for (auto &&[i, r] : zip(cfg->atoms(), refCoords))
        i.setCoordinates(r);
    cfg->updateObjectRelationships();

    // Create an override potential to describe the Ar-OW interaction
    dissolve.coreData().addPairPotentialOverride("Ar", "OW", PairPotentialOverride::PairPotentialOverrideType::Replace,
                                                 {Functions1D::Form::LennardJones126, "epsilon=0.35 sigma=2.166"});

    // Prepare the main simulation
    EXPECT_TRUE(dissolve.prepare());

    // Test consistency of energy calculation with DL_POLY reference energies

    // Test range of cell sizes - should make no difference to total energy
    std::vector<std::tuple<double, double, double>> states = {
        {9.0, 7.0, -2.0324775}, {9.0, 6.0, -2.0324775}, {9.0, 5.0, -2.0324775}, {9.0, 4.0, -2.0324775}, {9.0, 3.0, -2.0324775},
        {7.0, 7.0, -1.993993},  {7.0, 6.0, -1.993993},  {7.0, 5.0, -1.993993},  {7.0, 4.0, -1.993993},  {5.0, 7.0, -1.904968},
        {5.0, 6.0, -1.904968},  {5.0, 5.0, -1.904968},  {5.0, 4.0, -1.904968}};
    for (auto &state : states)
    {
        auto [rCut, cellSize, refEnergy] = state;

        // Initialise an EnergyKernel with the specified cutoff
        auto kernel = KernelProducer::energyKernel(cfg, dissolve.worldPool(), dissolve.potentialMap(), rCut);

        // Regenerate cells to new size spec and re-assign atoms
        cfg->cells().generate(cfg->box(), cellSize, dissolve.pairPotentialRange());
        cfg->updateAtomLocations(true);

        // Calculate total Cell-based energy
        EXPECT_NEAR(refEnergy, kernel->totalPairPotentialEnergy(false, ProcessPool::PoolStrategy).total(), 1.0e-4);

        // Calculate atomic energy from the Ar
        EXPECT_NEAR(refEnergy, kernel->totalEnergy(cfg->atom(0)).total(), 1.0e-4);
    }
}

